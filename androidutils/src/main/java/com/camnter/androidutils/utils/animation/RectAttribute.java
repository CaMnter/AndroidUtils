public class RectAttribute {
    public float left;
    public float top;
    public float right;
    public float bottom;
    public float radius;


    public RectAttribute(float left, float top, float right, float bottom, float radius) {
        this.left = left;
        this.top = top;
        this.right = right;
        this.bottom = bottom;
        this.radius = radius;
    }
}
